/****************************************
* 将 num 直接截取到小数点后 n 位
****************************************/

var breakFloat = function (num, n) {
    return num.toString().replace( new RegExp( "^(\\-?\\d*\\.?\\d{0,"+ n +"})(\\d*)$" ), "$1");
};

